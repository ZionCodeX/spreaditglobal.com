<p>
     <h3>
    <a href="#">Which Countries do we ship to?</a>
    </h3>
</p>
<p>
We ship to the US, Canada, Mexico, the UK, Nigeria, Ghana, Zimbabwe, etc. You can pay for your order in USD or Naira (for our Nigerian customers).
</p>

<p>
     <h3>
    <a href="#">Who do we serve?</a>
    </h3>
</p>
<p>
We are primarily a B2B platform. We serve people who want to buy things in bulk from China. If you run an e-commerce website and are looking to dropship, we may not be your best partners, The reason is that the main website we help you import from, 1688.com, is a wholesale website. More so, we are able to ship directly to you, goods that weigh at least 10kg. Why 10kg? 10kg is the minimum weight that gives us the best shipping rate to your destination using DHL. The shipping rate to Canada and the US is $15/kg for a minimum weight of 10kg. The only exception to this is if we are shipping to Nigeria. Nigerian customers are free to order products that meet the minimum order quantity stated by suppliers and we will ship to them no matter the weight. Customers in Nigeria can pick up their order from our Lagos office or we can arrange home delivery from our Lagos office at an extra cost. We do not do door-to-door delivery for customers in Nigeria from China.
</p>
<p>
     <h3>
    <a href="#">Why import through Spreadit?</a>
    </h3>
</p>
<p>
We communicate with Chinese sellers and manufacturers on your behalf. It can really be hard trying to communicate with the Chinese in English. Because our China office is staffed with Chinese, it’s easier for our staff to communicate with the sellers. We consider this very crucial in doing business with Chinese business people, especially those who sell on 1688.com and the other Chinese websites we help you buy from.
</p>
<p>
We help place orders and have sellers ship directly to the office in China. We help make payments to sellers. Sellers on 1688.com only accept RMB/CNY. They will not accept payments in any other currency. We consolidate all items from different sellers and ship them together so you can save shipping costs.
</p>
<p>
We inspect the products we order on your behalf to ensure they are exactly what we have ordered. Note that we do not inspect the functionality of what we order on your behalf as all inspections are superficial. There are times when the products you have ordered are not available at the time you placed the order; in such cases, we will promptly inform you and suggest that you either change your order or that we buy from another seller. In the event that no other seller has the particular product you want, we will inform you and refund you within 7 business days.
</p>
<p>
We accept payments in USD via PayPal and Naira via direct bank deposit or debit cards and bear the burden of sourcing Yuan and remitting money overseas. So, while products are sold in CNY on 1688.com, you pay in USD or Naira and we handle the rest.
</p>
<p>
We also take care of all customs issues on your behalf; you simply pick up your products at our Lagos office (for Nigerian customers) or straight to your address for US, UK, and  Canadian customers via DHL. We can also arrange for home delivery from our Lagos office to your location for our Nigerian customers.
</p>

<p>
     <h3>
    <a href="#">We also offer a Shipping only service.</a>
    </h3>
</p>
<p>
If you already have suppliers and want us to ship to your location, you can give our China office address to your supplier. Then create a Shipping only order on our website.
</p>

